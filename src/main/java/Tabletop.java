public class Tabletop {

    private int x;
    private int y;
    public static final int ORIGIN_X = 0;
    public static final int ORIGIN_Y = 0;

    public Tabletop(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
